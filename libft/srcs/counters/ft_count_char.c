#include <libft.h>

size_t	ft_count_char(long nb)
{
	size_t	a;

	a = 0;
	if (nb <= 0)
	{
		nb = nb * -1;
		a++;
	}
	while (nb > 0)
	{
		nb = nb / 10;
		a++;
	}
	return (a);
}
