#include <libft.h>

int	ft_isprime(int nb)
{
	int	a;

	a = 2;
	if (nb < a)
		return (0);
	while (a <= nb / a)
	{
		if (nb % a == 0)
			return (0);
		a++;
	}
	return (1);
}
